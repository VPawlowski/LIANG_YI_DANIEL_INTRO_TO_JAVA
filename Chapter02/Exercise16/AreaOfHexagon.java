package com.liang.y.daniel.chapter.two;

/* Write a program that prompts the user to enter the side of a hexagon and displays its area. The formula for computing
the area of a hexagon is:

    Area = ((3sqrt(3)) / 2) * s^2

where s is the length of a side. */

import java.util.Scanner;

public class AreaOfHexagon {
    public static void main(String[] args) {
        System.out.print("Enter the length of the hexagon's side: ");
        Scanner in = new Scanner(System.in);
        double sideLength = in.nextDouble();
        double hexagonArea = (3 * Math.pow(3, 0.5) / 2) * Math.pow(sideLength, 2);
        System.out.println("The area of the hexagon is: " + hexagonArea);
    }
}
