package com.liang.y.daniel.chapter.two;

/* Write a program that prompts the user to enter the distance to drive, the fuel efficiency of the car in miles per
gallon, and the price per gallon then displays the cost of the trip. */

import java.util.Scanner;

public class FuelCalculator {
    public static void main(String[] args) {
        System.out.print("Enter the total distance, in miles, driven: ");
        Scanner in = new Scanner(System.in);
        double totalDistance = in.nextDouble();
        System.out.print("Enter the fuel efficiency (miles per gallon) of the car: ");
        double fuelEfficiency = in.nextDouble();
        System.out.print("Enter the price of fuel per gallon: ");
        double fuelPrice = in.nextDouble();
        double totalPrice = totalDistance / fuelEfficiency * fuelPrice;
        System.out.println("The cost of driving is: $" + totalPrice);
    }
}
